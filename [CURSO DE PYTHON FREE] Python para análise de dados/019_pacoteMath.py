import math

'''O Python possui um conjunto de funções matemáticas integradas, incluindo um extenso módulo matemático, 
que permite realizar tarefas matemáticas em números.'''

tupla = (10, 5, 20, 30, 40)

print(min(tupla))  # valor minimo
print(max(tupla))  # valor máximo

print(abs(-7.25))  # muda sinal

print(pow(3, 4))  # potenciação

print(math.sqrt(81))  # raiz quadrada

print(math.ceil(1.4))  # arredonda para cima

print(math.ceil(1.4))  # arredonda para baixo

print(math.pi)
