#OPERADORES DE COMPARAÇÃO
'''Os operadores de comparação são usados para comparar dois valores: '''

#1 '==' ( Igual a )
print(10 == 10, 10 == 8)

#2 '!=' ( Diferente )
print(10 != 8, 10 != 10)

#3 '>' ( Maior )
print(10 > 11)

#4 '<' ( Menor )

print(10 < 5)

#5 '>=' ( Maior igual )
print(15 >= 17, 15 >= 14)

#6 '<=' ( Menor igual)
print(20 <= 15, 20 <= 20)