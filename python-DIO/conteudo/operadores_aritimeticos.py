produto_1 = 25
produto_2 = 10

# Soma
print(produto_1 + produto_2)

# Subtração
print(produto_1 - produto_2)

# Divisão e Divisão Inteira
print(produto_1 / produto_2)
print(produto_1 // produto_2)

# Multiplicação
print(produto_1 * produto_2)

# Módulo
print(produto_1 % produto_2)

# Exponenciação
print(produto_1 ** produto_2)


'''
Em Python, os operadores são avaliados na seguinte ordem:
1° = **
2° = *, /, //
3° = %
4° = + ,-
5° = comparações na ordem que aparecem
not, and, or
'''

x = (10 + 5) * 4
y = (10 / 2) + (25 * 2) - (2 ** 2)
print(x)
print(y)